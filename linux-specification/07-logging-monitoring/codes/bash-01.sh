# 日志级别 (从高到低):
# 0: emerg  - 系统不可用
# 1: alert  - 需要立即处理
# 2: crit   - 严重
# 3: err    - 错误
# 4: warning - 警告
# 5: notice - 普通通知
# 6: info   - 信息
# 7: debug  - 调试

# facilities (设施):
# auth      - 认证 (login, su, sudo)
# authpriv  - 私有认证
# cron      - 定时任务
# daemon    - 系统守护进程
# ftp       - FTP服务
# kern      - 内核
# local0-7  - 本地使用
# mail      - 邮件
# news      - 新闻组
# syslog    - syslog内部
# user      - 用户进程
# uucp      - UUCP