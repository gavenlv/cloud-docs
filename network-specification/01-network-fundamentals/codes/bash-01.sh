# 第1层 - 物理层: 负责比特的传输
# 设备: 网线、光纤、集线器、Repeater
# 信号: 电信号、光信号、无线电波

# 第2层 - 数据链路层: 负责帧的传输和MAC寻址
# 设备: 交换机、网桥
# 地址: MAC地址 (48位, 如: 00:1A:2B:3C:4D:5E)
# 协议: Ethernet, PPP, HDLC

# 第3层 - 网络层: 负责IP寻址和路由
# 设备: 路由器、三层交换机
# 地址: IP地址 (IPv4: 32位, IPv6: 128位)
# 协议: IP, ICMP, ARP, RIP, OSPF, BGP

# 第4层 - 传输层: 负责端到端的可靠传输
# 协议: TCP (可靠), UDP (不可靠)
# 端口: 16位 (0-65535)
# 知名端口: HTTP(80), HTTPS(443), SSH(22), DNS(53)

# 第5-7层 - 应用层: 负责具体应用服务
# 协议: HTTP, FTP, SMTP, POP3, IMAP, DNS, SSH, Telnet